import csv
import io
import uuid
from datetime import datetime, timezone

def _na(value, fmt=None):
    if value is None or value == "" or value == [] or value == {}:
        return "N/A"
    if fmt:
        try:
            return fmt(value)
        except Exception:
            return str(value)
    return str(value)


def _fmt_ts(raw):
    if not raw or raw == "N/A":
        return "N/A"
    s = str(raw).strip()

    import re as _re
    s = _re.sub(r'(\.\d{6})\d+', r'\1', s)
    s = _re.sub(r'[+-]00:?00$', 'Z', s)
    has_tz = s.endswith('Z')
    s_clean = _re.sub(r'[+-]\d{2}:?\d{2}$', '', s) if not has_tz else s

    for fmt in (
        "%Y-%m-%dT%H:%M:%S.%fZ",
        "%Y-%m-%dT%H:%M:%SZ",
        "%Y-%m-%dT%H:%M:%S.%f",
        "%Y-%m-%dT%H:%M:%S",
        "%Y-%m-%d %H:%M:%S.%f",
        "%Y-%m-%d %H:%M:%S",
        "%Y-%m-%d %H:%M",
        "%Y-%m-%d",
    ):
        target = s if fmt.endswith('Z') else s_clean
        try:
            dt = datetime.strptime(target, fmt)
            suffix = " UTC" if has_tz else ""
            return dt.strftime(f"%-d %b %Y, %H:%M:%S{suffix}")
        except ValueError:
            continue
    return str(raw)  # return original if no format matched


def _sev_label(sev_int):
    mapping = {1: "HIGH", 2: "MEDIUM", 3: "LOW"}
    return mapping.get(int(sev_int) if sev_int is not None else 3, "LOW")


def _now_utc():
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")


def _dedup_alerts(flagged_entities):
    """Extract, deduplicate and chronologically sort all Suricata alerts."""
    all_alerts = []
    for entity in (flagged_entities or []):
        for alert in entity.get("alerts", []):
            alert = dict(alert)
            alert["_entity_ip"] = entity.get("ip", "N/A")
            all_alerts.append(alert)
    all_alerts.sort(key=lambda a: a.get("timestamp") or "")
    seen = set()
    deduped = []
    for a in all_alerts:
        key = (a.get("signature"), a.get("src_ip"), a.get("timestamp"))
        if key not in seen:
            seen.add(key)
            deduped.append(a)
    return deduped


