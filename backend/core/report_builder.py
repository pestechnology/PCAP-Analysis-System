def build_csv(data: dict, pcap_filename: str = "unknown.pcap") -> bytes:
    buf = io.StringIO()
    writer = csv.writer(buf, quoting=csv.QUOTE_MINIMAL)
    report_id = str(uuid.uuid4())
    timestamp = _now_utc()

    def section(title):
        writer.writerow([])
        writer.writerow([f"=== {title} ==="])

    # ---- REPORT METADATA ----
    section("REPORT METADATA")
    writer.writerow(["Report ID", report_id])
    writer.writerow(["Generated At (UTC)", timestamp])
    writer.writerow(["PCAP File", _na(pcap_filename)])
    writer.writerow(["Generated By", "Adaptive PCAP Analysis System v1.0"])
    writer.writerow(["Classification", "CONFIDENTIAL: For Authorized SOC Use Only"])

    # ---- CAPTURE METADATA ----
    section("CAPTURE METADATA")
    meta = data.get("capture_metadata", {})
    writer.writerow(["Field", "Value"])
    writer.writerow(["PCAP Format", _na(meta.get("pcap_format"))])
    writer.writerow(["File Size (KB)", _na(meta.get("file_size_kb"))])
    writer.writerow(["Capture Duration (s)", _na(meta.get("capture_duration"))])
    writer.writerow(["Start Time", _fmt_ts(meta.get("start_time"))])
    writer.writerow(["End Time", _fmt_ts(meta.get("end_time"))])
    writer.writerow(["Snapshot Length (bytes)", _na(meta.get("snapshot_length"))])

    # ---- SUMMARY METRICS ----
    section("SUMMARY METRICS")
    writer.writerow(["Metric", "Value"])
    writer.writerow(["Total Packets", _na(data.get("total_packets"))])
    writer.writerow(["Valid Packets", _na(data.get("valid_packets"))])
    writer.writerow(["Malformed Packets", _na(data.get("malformed_packets"))])
    writer.writerow(["Fragmented Packets", _na(data.get("fragmented_packets"))])
    writer.writerow(["Jumbo Frames (>1500B)", _na(data.get("jumbo_frames"))])
    writer.writerow(["Packets Per Second", _na(data.get("packets_per_second"))])
    pkt = data.get("packet_size", {})
    writer.writerow(["Packet Size — Min (bytes)", _na(pkt.get("min"))])
    writer.writerow(["Packet Size — Max (bytes)", _na(pkt.get("max"))])
    writer.writerow(["Packet Size — Avg (bytes)", _na(pkt.get("avg"))])

    # ---- PACKET SIZE HISTOGRAM ----
    section("PACKET SIZE HISTOGRAM")
    hist = data.get("packet_size_histogram", {})
    if hist:
        writer.writerow(["Bin", "Packet Count"])
        for bin_label, count in hist.items():
            writer.writerow([bin_label, count])
    else:
        writer.writerow(["No histogram data available"])

    # ---- IP ADDRESS DISTRIBUTION ----
    section("IP ADDRESS DISTRIBUTION")
    ip_dist = data.get("ip_distribution", {})
    if ip_dist:
        writer.writerow(["IP Class", "Packet Count"])
        for cls, cnt in ip_dist.items():
            writer.writerow([cls, cnt])
    else:
        writer.writerow(["No IP distribution data"])

    # IP percentages
    ip_pct = data.get("ip_percentages", {})
    if ip_pct:
        writer.writerow([])
        writer.writerow(["IP Class", "Percentage (%)"])
        for cls, pct in ip_pct.items():
            writer.writerow([cls, pct])

    # ---- PROTOCOL DISTRIBUTION ----
    section("PROTOCOL DISTRIBUTION")
    proto_dist = data.get("protocol_distribution", {})
    if proto_dist:
        writer.writerow(["Protocol", "Packet Count"])
        for proto, count in sorted(proto_dist.items(), key=lambda x: -x[1]):
            writer.writerow([proto, count])
    else:
        writer.writerow(["No protocol distribution data"])

    # ---- PROTOCOL ACTIVITY TIMELINE ----
    section("PROTOCOL ACTIVITY TIMELINE")
    timeline = data.get("protocol_timeline", {})
    if timeline:
        writer.writerow(["Time Bucket", "Protocol", "Count"])
        for time_bucket, protocols in sorted(timeline.items()):
            # time_bucket is usually an epoch string e.g. "1771234640"
            try:
                human_time = datetime.fromtimestamp(int(time_bucket), tz=timezone.utc).strftime("%H:%M:%S")
            except:
                human_time = time_bucket

            if isinstance(protocols, dict):
                for proto, cnt in protocols.items():
                    writer.writerow([human_time, proto, cnt])
            else:
                writer.writerow([human_time, "—", protocols])
    else:
        writer.writerow(["No protocol timeline data available"])

    # ---- TCP ANALYSIS ----
    section("TCP ANALYSIS")
    tcp = data.get("tcp", {})
    if tcp:
        writer.writerow(["Metric", "Value"])
        writer.writerow(["Total TCP Packets", _na(tcp.get("total_tcp_packets"))])
        writer.writerow(["Graceful Closes (FIN)", _na(tcp.get("graceful_closes"))])
        writer.writerow(["Abrupt Resets (RST)", _na(tcp.get("rst_closes"))])
        writer.writerow(["Timeout Retransmissions", _na(tcp.get("timeout_retransmissions"))])
        writer.writerow(["Fast Retransmissions", _na(tcp.get("fast_retransmissions"))])
        writer.writerow(["Partial Retransmissions", _na(tcp.get("partial_retransmissions"))])
        writer.writerow(["Out-of-Order Packets", _na(tcp.get("out_of_order"))])
        writer.writerow(["Retransmission Rate (%)", _na(tcp.get("retransmission_rate"))])
        writer.writerow(["Severity", _na(tcp.get("severity")).upper()])
        writer.writerow(["Assessment Note", _na(tcp.get("note"))])
    else:
        writer.writerow(["No TCP analysis data available"])

    # ---- LAYER 2 ANALYSIS ----
    section("LAYER 2 ANALYSIS")
    l2_link = data.get("link_layer", {})
    writer.writerow(["Link-Layer Type", _na(l2_link.get("primary"))])
    
    # ARP
    arp = data.get("arp_analysis", {})
    writer.writerow([])
    writer.writerow(["=== ARP ANALYSIS ==="])
    writer.writerow(["Total ARP Packets", _na(arp.get("total_arp_packets"))])
    writer.writerow(["Request/Reply Ratio", _na(arp.get("request_reply_ratio"))])
    spoof = arp.get("potential_spoofing", [])
    if spoof:
        writer.writerow(["POTENTIAL ARP SPOOFING DETECTED"])
        writer.writerow(["IP Address", "Competing MAC Addresses"])
        for s in spoof:
            writer.writerow([_na(s.get("ip")), "| ".join(s.get("macs", []))])

    # VLAN
    vlan = data.get("vlan_analysis", {})
    writer.writerow([])
    writer.writerow(["=== VLAN / 802.1Q ANALYSIS ==="])
    writer.writerow(["Total Tagged Frames", _na(vlan.get("total_tagged_frames"))])
    writer.writerow(["Unique VLAN IDs", _na(vlan.get("unique_vlan_count"))])
    writer.writerow(["QinQ (Double Tagging) Frames", _na(vlan.get("double_tagged_frames"))])

    # STP
    stp = data.get("stp_analysis", {})
    writer.writerow([])
    writer.writerow(["=== STP (802.1D) ANALYSIS ==="])
    writer.writerow(["Total BPDUs", _na(stp.get("total_bpdus"))])
    writer.writerow(["Topology Changes Detected", _na(stp.get("topology_changes"))])

    # ---- SCTP ANALYSIS ----
    section("SCTP ANALYSIS")
    sctp = data.get("sctp_analysis", {})
    if sctp and sctp.get("total_sctp_packets", 0) > 0:
        writer.writerow(["Metric", "Value"])
        writer.writerow(["Total SCTP Packets", _na(sctp.get("total_sctp_packets"))])
        writer.writerow(["Unique Associations", _na(sctp.get("unique_associations"))])
        writer.writerow(["Multi-homing Indicators", _na(sctp.get("multi_homing_indicators"))])
        
        # Chunks
        writer.writerow([])
        writer.writerow(["Chunk Type", "Count"])
        for ct, cnt in sctp.get("chunk_types", {}).items():
            writer.writerow([ct, cnt])
    else:
        writer.writerow(["No SCTP activity detected"])

    # ---- TLS/SSL INSIGHTS ----
    section("TLS/SSL INSIGHTS")
    tls = data.get("tls_metadata", {})
    if tls:
        # Versions
        versions = tls.get("versions", {})
        if versions:
            writer.writerow(["TLS Version", "Connection Count"])
            for v, c in sorted(versions.items(), key=lambda x: -x[1]):
                writer.writerow([v, c])
        # Cipher Suites
        ciphers = tls.get("cipher_suites", {})
        if ciphers:
            writer.writerow([])
            writer.writerow(["Cipher Suite", "Count"])
            for cs, c in sorted(ciphers.items(), key=lambda x: -x[1]):
                writer.writerow([cs, c])
        # SNI Domains
        sni = tls.get("sni_domains", {})
        if sni:
            writer.writerow([])
            writer.writerow(["SNI Domain", "Connection Count"])
            for d, c in sorted(sni.items(), key=lambda x: -x[1]):
                writer.writerow([d, c])
        # JA3 Fingerprints
        ja3 = tls.get("ja3_fingerprints", {})
        if ja3:
            writer.writerow([])
            writer.writerow(["JA3 Fingerprint", "Count"])
            for f, c in sorted(ja3.items(), key=lambda x: -x[1]):
                writer.writerow([f, c])
        # Certificate Issuers
        issuers = tls.get("cert_issuers", {})
        if issuers:
            writer.writerow([])
            writer.writerow(["Certificate Issuer", "Count"])
            for issuer, c in sorted(issuers.items(), key=lambda x: -x[1]):
                writer.writerow([issuer, c])
        # Certificate Subjects
        subjects = tls.get("cert_subjects", {})
        if subjects:
            writer.writerow([])
            writer.writerow(["Certificate Subject (CN)", "Count"])
            for subj, c in sorted(subjects.items(), key=lambda x: -x[1]):
                writer.writerow([subj, c])
    else:
        writer.writerow(["No TLS/SSL metadata available"])

    # ---- HTTP THREAT ANALYSIS ----
    section("HTTP THREAT ANALYSIS (UNENCRYPTED MALICIOUS ACTIVITY)")
    http_threats = data.get("http_threats", [])
    if http_threats:
        writer.writerow(["Threat Type", "Source IP", "Source Port",
                         "Destination IP", "Destination Port",
                         "HTTP Host", "HTTP URI", "Method",
                         "Severity", "Timestamp"])
        for h in http_threats:
            writer.writerow([
                _na(h.get("threat_type")),
                _na(h.get("src_ip")),
                _na(h.get("src_port")),
                _na(h.get("dest_ip")),
                _na(h.get("dest_port")),
                _na(h.get("http_host")),
                _na(h.get("http_uri")),
                _na(h.get("http_method")),
                _na(h.get("severity")),
                _fmt_ts(h.get("timestamp")),
            ])
    else:
        writer.writerow(["No unencrypted HTTP threats detected"])

    # ---- BEHAVIORAL ANOMALY DETECTION ----
    section("BEHAVIORAL ANOMALY DETECTION (TCP)")
    tcp_behavior = data.get("tcp_behavior", {})
    behav_flagged = tcp_behavior.get("flagged_entities", []) if tcp_behavior else []
    if behav_flagged:
        writer.writerow(["Source IP", "Risk Score", "Flags Detected",
                         "Unique Ports Contacted", "Handshake Failure Rate",
                         "Total SYNs", "Failed SYNs"])
        for e in behav_flagged:
            reasons = "; ".join(e.get("reasons", []))
            writer.writerow([
                _na(e.get("ip")),
                _na(e.get("score")),
                _na(reasons),
                _na(e.get("unique_ports")),
                _na(e.get("handshake_failure_rate")),
                _na(e.get("total_syns")),
                _na(e.get("failed_syns")),
            ])
    else:
        writer.writerow(["No behavioral anomalies detected"])

    # ---- UDP ANALYSIS ----
    section("UDP ANALYSIS — SUMMARY")
    udp = data.get("udp_analysis", {})
    udp_summary = udp.get("udp_summary", {}) if udp else {}
    if udp_summary:
        writer.writerow(["Metric", "Value"])
        for k, v in udp_summary.items():
            writer.writerow([str(k).replace("_", " ").title(), _na(v)])
    else:
        writer.writerow(["No UDP summary data available"])

    # UDP status / severity
    udp_status = udp.get("status", "") if udp else ""
    if udp_status:
        writer.writerow(["UDP Status", udp_status.upper()])

    # Top UDP Sources
    top_udp_sources = udp.get("top_udp_sources", []) if udp else []
    if top_udp_sources:
        writer.writerow([])
        writer.writerow(["=== UDP — TOP SOURCES ==="])
        writer.writerow(["Source IP", "Packet Count"])
        for entry in top_udp_sources:
            if isinstance(entry, (list, tuple)) and len(entry) >= 2:
                writer.writerow([entry[0], entry[1]])
            elif isinstance(entry, dict):
                writer.writerow([_na(entry.get("ip")), _na(entry.get("count"))])

    # UDP Port Distribution
    port_dist = udp.get("port_distribution", []) if udp else []
    if port_dist:
        writer.writerow([])
        writer.writerow(["=== UDP — PORT DISTRIBUTION ==="])
        writer.writerow(["Port", "Packet Count"])
        for entry in port_dist:
            if isinstance(entry, (list, tuple)) and len(entry) >= 2:
                writer.writerow([entry[0], entry[1]])
            elif isinstance(entry, dict):
                writer.writerow([_na(entry.get("port")), _na(entry.get("count"))])

    # UDP Flagged / Suspicious Entities
    udp_flagged = udp.get("flagged_entities", []) if udp else []
    udp_suspicious = udp.get("suspicious_entities", []) if udp else []
    all_udp_flagged = udp_flagged + [e for e in udp_suspicious if e not in udp_flagged]
    if all_udp_flagged:
        writer.writerow([])
        writer.writerow(["=== UDP — FLAGGED ENTITIES ==="])
        writer.writerow(["IP Address", "Risk Score", "Flags / Reasons"])
        for e in all_udp_flagged:
            reasons = "; ".join(e.get("reasons", [])) if isinstance(e.get("reasons"), list) else _na(e.get("reasons"))
            writer.writerow([_na(e.get("ip")), _na(e.get("score")), reasons])

    # UDP Priority Targets
    priority = udp.get("priority_targets", []) if udp else []
    if priority:
        writer.writerow([])
        writer.writerow(["=== UDP — PRIORITY TARGETS ==="])
        writer.writerow(["IP Address", "Risk Score", "Reasons"])
        for e in priority:
            reasons = "; ".join(e.get("reasons", [])) if isinstance(e.get("reasons"), list) else _na(e.get("reasons"))
            writer.writerow([_na(e.get("ip")), _na(e.get("score")), reasons])

    # ---- SURICATA IDS ALERTS ----
    section("SURICATA IDS ALERTS & INDICATORS OF COMPROMISE")
    flagged_entities = (data.get("abnormal_activity") or {}).get("flagged_entities", [])
    deduped_alerts = _dedup_alerts(flagged_entities)

    if deduped_alerts:
        writer.writerow([
            "Entity IP", "Signature", "Severity", "Category",
            "SID", "Rev", "Source IP", "Source Port",
            "Destination IP", "Destination Port", "Protocol",
            "Flow ID", "Timestamp"
        ])
        for a in deduped_alerts:
            writer.writerow([
                _na(a.get("_entity_ip")),
                _na(a.get("signature")),
                _sev_label(a.get("severity", 3)),
                _na(a.get("category")),
                _na(a.get("sid")),
                _na(a.get("rev")),
                _na(a.get("src_ip")),
                _na(a.get("src_port")),
                _na(a.get("dest_ip")),
                _na(a.get("dest_port")),
                _na(a.get("proto")),
                _na(a.get("flow_id")),
                _fmt_ts(a.get("timestamp")),
            ])
    else:
        writer.writerow(["No Suricata alerts generated for this capture"])

    # ---- DNS & DOMAIN INTELLIGENCE ----
    section("DNS QUERY LOG")
    domains = data.get("domains", [])
    if domains:
        writer.writerow(["Observed DNS Domain"])
        for d in sorted(set(str(d) for d in domains)):
            writer.writerow([d])
    else:
        writer.writerow(["No DNS queries observed"])

    section("DOMAIN THREAT INTELLIGENCE")
    domain_alerts = data.get("domain_threat_alerts", [])
    if domain_alerts:
        writer.writerow(["Domain", "Source", "Classification", "Context"])
        for a in domain_alerts:
            writer.writerow([
                _na(a.get("domain")),
                _na(a.get("source")),
                _na(a.get("classification")).upper(),
                _na(a.get("context") or a.get("note")),
            ])
    else:
        writer.writerow(["No domain threats detected"])

    # ---- EXTRACTED URLS ----
    section("EXTRACTED HTTP URLs")
    urls = data.get("urls", [])
    if urls:
        writer.writerow(["URL", "Method", "Host", "Source IP", "Destination IP", "Timestamp"])
        for u in urls:
            if isinstance(u, dict):
                writer.writerow([
                    _na(u.get("url") or u.get("uri")),
                    _na(u.get("method")),
                    _na(u.get("host")),
                    _na(u.get("src_ip")),
                    _na(u.get("dest_ip")),
                    _fmt_ts(u.get("timestamp")),
                ])
            else:
                writer.writerow([str(u)])
    else:
        writer.writerow(["No HTTP URLs extracted"])

    # ---- HTTP TRANSACTIONS ----
    section("HTTP TRANSACTIONS")
    http_trans = data.get("http_transactions", [])
    if http_trans:
        writer.writerow(["Stream Index", "Client IP", "Client Port", "Server IP", "Server Port", "Server Host", "Method", "URL"])
        for t in http_trans:
            writer.writerow([
                _na(t.get("stream_index")),
                _na(t.get("client_ip")),
                _na(t.get("client_port")),
                _na(t.get("server_ip")),
                _na(t.get("server_port")),
                _na(t.get("server_host")),
                _na(t.get("method")),
                _na(t.get("url"))
            ])
    else:
        writer.writerow(["No HTTP transactions extracted"])

    # ---- EXTRACTED FILES ----
    section("EXTRACTED FILES")
    extracted_files = data.get("extracted_files", [])
    if extracted_files:
        writer.writerow(["Filename", "Size (Bytes)", "Protocol"])
        for f in extracted_files:
            writer.writerow([_na(f.get("filename")), _na(f.get("size_bytes")), _na(f.get("protocol"))])
    else:
        writer.writerow(["No files extracted"])

    # ---- UNENCRYPTED CREDENTIALS ----
    section("UNENCRYPTED CREDENTIALS")
    extracted_credentials = data.get("extracted_credentials", [])
    if extracted_credentials:
        writer.writerow(["Protocol", "Client", "Server", "Username", "Password Snippet"])
        for c in extracted_credentials:
            writer.writerow([_na(c.get("protocol")), _na(c.get("client")), _na(c.get("server")), _na(c.get("username")), _na(c.get("password_snippet"))])
    else:
        writer.writerow(["No unencrypted credentials extracted"])

    # ---- GEO DATA ----
    section("GEOGRAPHIC IP INTELLIGENCE")
    geo_data = data.get("geo_data", {})
    if geo_data:
        writer.writerow(["IP Address", "Country", "Country Code", "City", "ASN", "Organization"])
        for ip, geo in geo_data.items():
            if isinstance(geo, dict):
                writer.writerow([
                    ip,
                    _na(geo.get("country")),
                    _na(geo.get("country_code")),
                    _na(geo.get("city")),
                    _na(geo.get("asn")),
                    _na(geo.get("org") or geo.get("organization")),
                ])
    else:
        writer.writerow(["No geographic IP data available"])

    # ---- COUNTRY TRAFFIC DISTRIBUTION ----
    section("COUNTRY TRAFFIC DISTRIBUTION")
    country_traffic = data.get("country_traffic", [])
    if country_traffic:
        writer.writerow(["Country", "Packet Count"])
        for entry in country_traffic:
            if isinstance(entry, (list, tuple)) and len(entry) >= 2:
                writer.writerow([entry[0], entry[1]])
            elif isinstance(entry, dict):
                writer.writerow([_na(entry.get("country")), _na(entry.get("count"))])
    else:
        writer.writerow(["No country traffic data available"])

    # ---- TOP SENDERS / RECEIVERS ----
    section("TOP SENDERS (by Packet Count)")
    top_senders = data.get("top_senders", [])
    if top_senders:
        writer.writerow(["IP Address", "Packet Count"])
        for ip, count in top_senders:
            writer.writerow([ip, count])
    else:
        writer.writerow(["No sender data available"])

    section("TOP RECEIVERS (by Packet Count)")
    top_receivers = data.get("top_receivers", [])
    if top_receivers:
        writer.writerow(["IP Address", "Packet Count"])
        for ip, count in top_receivers:
            writer.writerow([ip, count])
    else:
        writer.writerow(["No receiver data available"])

    # ---- MAC VENDOR IDENTIFICATION ----
    section("MAC VENDOR IDENTIFICATION")
    mac_vendors = data.get("mac_vendors", [])
    if mac_vendors:
        # Data is [(vendor_string, count), ...] from Counter.most_common()
        writer.writerow(["Vendor / Manufacturer (OUI Prefix)", "Packet Count"])
        for entry in mac_vendors:
            if isinstance(entry, (list, tuple)) and len(entry) >= 2:
                writer.writerow([_na(entry[0]), _na(entry[1])])
            elif isinstance(entry, dict):
                vendor = entry.get("vendor") or entry.get("manufacturer") or entry.get("mac", "N/A")
                count = entry.get("count", "N/A")
                writer.writerow([_na(vendor), _na(count)])
    else:
        writer.writerow(["No MAC vendor data available"])

    return buf.getvalue().encode("utf-8")


